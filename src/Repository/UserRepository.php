  /**
     * get all posts
     *
     * @return array
     */
    public function findAllPosts()
    {
        return $this->getEntityManager()
            ->createQuery(
                'SELECT a
         FROM App:User a   
      
         ORDER BY a.id DESC'
            )
            ->getArrayResult();
    }


    public function findEntitiesByString($str){
        return $this->getEntityManager()
            ->createQuery(
                'SELECT p
                FROM App:User p
                WHERE p.nom LIKE :str OR p.prenom LIKE :str'
            )
            ->setParameter('str', '%'.$str.'%')
            ->getResult();
    }

